#!/usr/bin/env node
// fkit — install-cli-skills.mjs
//
// Installs fkit's own CLI skills (fkit-install, fkit-update) into the user's
// GLOBAL skill directories so `/fkit-install` and `/fkit-update` work in ANY
// project — including a fresh one where fkit is not yet set up.
//
// Unlike the per-project workflow skills (generic/skills/, compiled by
// compile-skills.mjs into a project), these are installed ONCE, globally, and
// have THIS fkit clone's absolute path baked in ({{fkit_home}}) so they always
// know where to find bootstrap.mjs / sync.mjs.
//
// Zero dependencies. Makes no commits. Usage:
//   node bin/install-cli-skills.mjs [--target claude|codex|both] [--dry-run]
//   node bin/install-cli-skills.mjs --claude-dir <dir> --codex-dir <dir>

import { readFileSync, writeFileSync, mkdirSync, existsSync, readdirSync } from "node:fs";
import { dirname, join, resolve } from "node:path";
import { fileURLToPath } from "node:url";
import { homedir } from "node:os";
import { parseYaml, splitFrontmatter, subVars } from "./lib.mjs";

const __dirname = dirname(fileURLToPath(import.meta.url));
const KIT = resolve(__dirname, "..");
const HOME = homedir();

const argv = process.argv.slice(2);
const getArg = (n, d) => { const i = argv.indexOf(n); return i >= 0 ? argv[i + 1] : d; };
const target = getArg("--target", "both");
const dryRun = argv.includes("--dry-run");
const claudeDir = resolve(getArg("--claude-dir", join(HOME, ".claude", "skills")));
const codexDir = resolve(getArg("--codex-dir", join(HOME, ".codex", "skills")));

const SRC = join(KIT, "cli-skills");
if (!existsSync(SRC)) {
  console.error(`no cli-skills/ directory at ${SRC}`);
  process.exit(1);
}

// These global skills are regenerated by THIS installer, not by `sync`.
const marker =
  "<!-- fkit:generated (cli-skill) — do NOT hand-edit; re-run " +
  "bin/install-cli-skills.mjs from the fkit repo to regenerate. -->";

const skills = readdirSync(SRC).filter((n) => existsSync(join(SRC, n, "skill.md")));

function emit(model, dir) {
  for (const name of skills) {
    const { fm, body } = splitFrontmatter(readFileSync(join(SRC, name, "skill.md"), "utf8"));
    const metaP = join(SRC, name, "meta.yml");
    const meta = existsSync(metaP) ? parseYaml(readFileSync(metaP, "utf8")) : {};

    // fkit_home is baked in; invoke + running_model differ per CLI.
    const vars = {
      fkit_home: KIT,
      invoke: model === "codex" ? "$" : "/",
      running_model: model === "codex" ? "Codex" : "Claude",
    };
    const description = subVars(
      (meta[model] && meta[model].description) || fm.description || "",
      vars,
    );
    const renderedBody = subVars(body, vars).replace(/^\n+/, "").replace(/\s*$/, "") + "\n";

    const fmLines = ["---", `name: ${fm.name || name}`, `description: ${description}`];
    if (model === "claude") fmLines.push("user-invocable: true");
    fmLines.push("---");
    const content = fmLines.join("\n") + "\n" + marker + "\n\n" + renderedBody;

    const outSkill = join(dir, name, "SKILL.md");
    console.log(`  ${model} → ${outSkill}`);
    if (!dryRun) {
      mkdirSync(dirname(outSkill), { recursive: true });
      writeFileSync(outSkill, content);
    }

    if (model === "codex") {
      const iface = (meta.codex && meta.codex.interface) || {};
      const yaml =
        [
          "interface:",
          `  display_name: ${JSON.stringify(subVars(iface.display_name || (fm.name || name), vars))}`,
          `  short_description: ${JSON.stringify(subVars(iface.short_description || "", vars))}`,
          `  default_prompt: ${JSON.stringify(subVars(iface.default_prompt || "", vars))}`,
        ].join("\n") + "\n";
      const outYaml = join(dir, name, "agents", "openai.yaml");
      if (!dryRun) {
        mkdirSync(dirname(outYaml), { recursive: true });
        writeFileSync(outYaml, yaml);
      }
    }
  }
}

console.log(dryRun ? "DRY RUN — no files written" : "Installing fkit CLI skills");
console.log(`fkit clone (baked into the skills): ${KIT}`);
if (target === "claude" || target === "both") {
  console.log(`\nClaude → ${claudeDir}`);
  emit("claude", claudeDir);
}
if (target === "codex" || target === "both") {
  console.log(`\nCodex → ${codexDir}`);
  emit("codex", codexDir);
}
console.log(
  `\n${dryRun ? "Would install" : "Installed"} ${skills.length} skill(s): ${skills.join(", ")}`,
);
console.log("Makes no commits. Re-run after moving the fkit clone or editing cli-skills/.");
